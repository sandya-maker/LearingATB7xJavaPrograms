public class Lab061 {
    public static void main(String[] args) {
        int a = 10;
        int result = a++;
        System.out.println(a);
        System.out.println(result);

        // Exp - 10 , a - 11

        int b = 10;
        int result1 = ++b;
        System.out.println(b);
        System.out.println(result1);

        //Exp - 11, b - 11

    }
}
